import turtle as t 
t.Screen()
t.setup(600,500,100,200)
t.bgcolor('SpringGreen')
t.title("Show Turtle")
t.shape('turtle')
t.forward(200)
t.right(90) #It's about angle
t.up()
t.forward(100)
t.down()
t.right(90)
t.forward(250)
t.bgcolor('blue')
t.up()
t.forward(100)
t.down()
t.right(135)
t.forward(150)
t.left(45)
t.backward(200)
t.done()
t.bye()